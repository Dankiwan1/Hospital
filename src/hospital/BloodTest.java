    private void combofill() {
        try {
            String combo = "select * from hospitaltests";
            pst = con.prepareStatement(combo);
            rs = pst.executeQuery();
            while (rs.next()) {
                String testname = rs.getString("TEST_NAME");
                combo_testname.addItem(testname);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void Search() {

        try {
            String sql1 = "SELECT patient.TITLE,patient.FIRST_NAME,patient.LAST_NAME,patient.PATIENT_ID from patient WHERE patient.PATIENT_ID=?";
            pst = con.prepareStatement(sql1);
            pst.setString(1, txt_search.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                pid = rs.getString("PATIENT_ID");

                titlee = rs.getString("TITLE");

                fname = rs.getString("FIRST_NAME");

                lname = rs.getString("LAST_NAME");

                user.setText(titlee + " " + fname + " " + lname + "  [" + pid + "]");

            } else {
                JOptionPane.showMessageDialog(null, "Sorry!! The patient ID does not exist");
                txt_search.setText("");
                combo_testname.setSelectedIndex(0);
                txt_result.setText("");
                txt_resultsotherdetails.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }

