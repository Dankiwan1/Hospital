 public void combofill() {
        try {
            String sql1 = "select test_name from hospitaltests ";
            pst = con.prepareStatement(sql1);
            rs = pst.executeQuery();
            while (rs.next()) {
                String name = rs.getString("test_name");
                tests_category.addItem(name);
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewPatient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
